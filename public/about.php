                <p>Az eszköz megalkotásakor kiterjesztő értelmezést vettünk alapul, és olyan közelítő mutatókat is alkalmaztunk, amelyeknek nem minden korrupciós ügyben van jelentőségük, vagy nem vezetnek elkerülhetetlenül korrupcióhoz. A jelzések egy-egy mutató kirívó értékére hívják fel a figyelmet, amely például összeférhetetlenség vagy korrupció gyanújára utalhat, de a jelzett esetek NEM tekinthetőek automatikusan korruptnak. Összességében, a jelzések potenciális veszélyforrásokat jelölnek, melyek magyarázatát az egyes mutatók leírásában fejtjük ki, de önmagukban nem jelentenek bizonyítékot bűncselekményre, korrupcióra vagy más visszaélésre. A figyelmeztetés azonban utal arra, hogy mit érdemes elkerülni a tisztességes verseny érdekében.</p>
                <p>A cégekre 4, a végső tulajdonosokra 3 különböző jelzés adható az oldalon.</p>
                <h3>Cégekre vonatkozó jelzések</h3>
                <ol>
                <li><b>100 százalék feletti közbeszerzési arány:</b> <i>A három éve alatt elnyert közbeszerzések értékének éves átlaga meghaladta a következő évi nettó árbevétel 100 százalékát</i></li>
                <li><b>20 százalék feletti nyereségráta:</b> <i>az adózott eredmény meghaladta a nettó árbevétel 20 százalékát</i></li>
                <li><b>5 milliárd forint feletti közbeszerzési összérték:</b> <i>Az elnyert közbeszerzések összértéke meghaladja az 5 milliárd forintot</i></li>
                <li><b>1 milliárd forint feletti közbeszerzési átlagérték:</b> <i>Az elnyert közbeszerzések egy eljárásra jutó értéke meghaladja az 1 milliárd forintot</i></li>
                </ol>
                <h3>Tulajdonosokra vonatkozó jelzések</h3>
                <ol>
                <li><b>5 milliárd forint feletti közbeszerzési összérték:</b> <i>A végsőnek számító tulajdonos (többségükben legalább 25 százalékban tulajdonolt) cégei együttesen több mint 5 milliárd forint értékben nyertek közbeszerzéseken.</i></li>
                <li><b>Közhatalmi pozíció:</b> <i>A végső tulajdonos betöltött legalább egy közhatalmi pozíciót a kormánytagoktól a megyei jogú városok jegyzőiig terjedő körben.</i></li>
                <li><b>Min. 2 említés a sajtóadatbázisban:</b> <i>A végső tulajdonos neve legalább kétszer megjelent a K-Monitor közpénzfelhasználásról és átláthatóságról szóló sajtóadatbázisában.</i></li>
