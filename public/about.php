                <p>A felületen látható grafikonok és mutatók kialakításához az alábbi adatforrásokat használtuk.</p> 
                <h3>1. Céginformációk</h3>
                <p>Nettó árbevételre, adózott eredményére, tulajdonosi háttérére vonatkozó adatok 2018 és 2021 között. Az adatok bárki számára azonos módon hozzáférhető, nyilvános cégjegyzéken alapuló forrásokból származnak a <a href="https://www.dnb.com/" target="_blank">Dun&Bradstreet</a> adatbázisából átvéve.</p>
                <h3>2. Közbeszerzési adatok</h3>
                <p>Az érintett cégek által ajánlattevőként 2018 és 2021 között, egyedül vagy konzorciumban elnyert közbeszerzési eljárások értéke, időpontja, tárgya, illetve a kapcsolódó ajánlatkérő szervezet megnevezése. Az adatok forrása a <a href="https://www.kozbeszerzes.hu/ertesito/" target="_blank">Közbeszerzési Hatóság honlapján</a> közzétett eredménytájékoztató hirdetmények, melyeket közvetett módon a Dun&Bradstreet adatbázisából vettünk át.</p>
                <h3>3. Közhatalmi kapcsolatok</h3>
                <p>A <a href="https://kozbeszerzes.hu/torveny/kbt-2015/2/4/" target="_blank">hatályos jogszabályok</a> nagyon szűken határozzák meg azokat az eseteket, amikor egy politikai vezető vagy köztisztviselő érdekeltsége egy közbeszerzési eljárásban kiválasztott vállalatban összeférhetetlennek minősülhet. Az összeférhetetlenség lényegében csak a köztársasági elnökre, a kormány tagjaira (a miniszterelnökre és a miniszterekre) és egyes állami szervek vezetőire vonatkozik. A TI Magyarország kiterjesztően értelmezi az összeférhetetlenséget. Az említett állami vezetőkön kívül a 2010. január 01. és 2022. szeptember 30. között foglalkoztatott volt és jelenlegi parlamenti képviselőket, kormánybiztosokat, államtitkárokat, helyettes államtitkárokat és kormányszóvivőket is bevontuk a vizsgálatba, melyhez az <a href="https://www.parlament.hu/orszaggyules" target="_blank">Országgyűlés</a> és <a href="https://kormany.hu/" target="_blank">kormany.hu</a> felületein aktív és a korábbi archív felületeken elérhető információkat gyűjtöttünk össze. Szoros politikai érintettségük miatt a közszereplők közé soroltuk a közelmúltban létrehozott közfeladatot ellátó <a href="https://modellvaltas.kormany.hu/alapitvanyok" target="_blank">közérdekű vagyonkezelő alapítványok</a> (KEKVA-k) kuratóriumának és felügyelőbizottságának tagjait és az ilyen alapítványok vagyonellenőreit, valamint az állami tulajdonú vállalatok vezetőit is. Az állami tulajdonú cégek vezetőire vonatkozó céginformációs adatok szintén a Dun&Bradstreet adatbázisából származnak. Ezen kívül a megyei jogú városok 2010. január 01. és 2022. szeptember 30. között regnáló polgármestereinek és jegyzőinek adatait is használtuk, melyeket a <a href="https://www.valasztas.hu/home" target="_blank">Nemzeti Választási Iroda</a> információi alapján gyűjtöttünk össze.</p> 
                <p>Emellett a <a href="https://adatbazis.k-monitor.hu/" target="_blank">K-Monitor sajtóadatbázisában</a> való megjelenésekkel kimutatható egyezéseket is megvizsgáltuk annak érdekében, hogy a közpénzek felhasználásához köthető, sajtóban feltárt kapcsolódásokra rámutassunk.</p>
                <p><b>A Transparency International Magyarország Alapítvány minden ésszerű intézkedést megtett annak érdekében, hogy a valóságnak megfelelő adatokat közöljön, valamint biztosítsa a személyes adatok védelmét. Kérjük, hogy amennyiben a közölt adatokban hibát talál, vagy a személyes adatok kezelésével kapcsolatos problémát kíván jelezni, vegye fel velünk a kapcsolatot a <a href="mailto:info@transparency.hu" target="_blank">info@transparency.hu</a> e-mail címen, hogy adatainkat korrigálhassuk.</b></p>
