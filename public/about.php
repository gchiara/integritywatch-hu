                <h3 class="mt-0">1. Cégadatokkal kapcsolatos korlátok</h3>
                <ul>
                <li>Cégbejegyzési és közzétételi sajátosságokat figyelembe véve a részvénytársaságok feltárt tulajdonosi szerkezete bizonyos esetekben eltérhet a valóságtól.</li>
                <li>A tulajdonosi hányad arányának mértéke sok esetben egyáltalán nem vagy nem pontosan ismert, csak a becsült érték (például maximum érték vagy küszöbérték).</li>
                <li>Egyes végső tulajdonosok több cégen keresztül különböző arányban érdekeltek egy vállalkozásban. Ilyen esetben a magasabb értéket szerepeltetjük.</li> 
                <li>A rendelkezésre álló vállalati adatok csak a magyar tulajdonosokra vonatkoznak. Ha a cég végső tulajdonosa külföldi cég, akkor nem ismerjük a végső tulajdonos kilétét, csak azt tudjuk, hogy külföldi szervezetről van szó, mivel a hazai jogszabályok nem teszik kötelezővé a külföldi vállalkozás végső tulajdonosainak közzétételét a nyilvános céginformációs adatbázisokban.</li>
                <li>Az adatok nem tartalmazzák az úgynevezett magántőkealapok kezelőszervének értékpapírjait birtokló tulajdonosokat és a bizalmi vagyonkezelők ügyfeleit sem, mivel azok tényleges tulajdonosait nem kötelező a hatályos jogszabályok alapján bejelenteni az alapításkor és a működés kapcsán.</li>
                <li>Adatvédelmi okok miatt névvel azokat a tulajdonosokat szerepeltetjük, akik (i) a vizsgált cégek többségében legalább 25 százalékos részesedéssel rendelkeznek, és cégeik összesen legalább 5 milliárd forint értékben nyertek el közbeszerzést a vizsgált időszakban; (ii) akik közhatalmi pozíciót töltenek/töltöttek be; (iii) akik legalább 2 említéssel szerepelnek a sajtóadatbázisban.</li>
                </ul>
                <h3>2. Közbeszerzési adatokkal kapcsolatos korlátok</h3>
                <ul>
                <li>Ha egy vállalat konzorciumban nyer el egy közbeszerzési szerződést, a beszerzés pontos összegéből való részesedés mértéke nem ismert. Ilyen esetekben proxy értéket használtunk: az érintett cégeknél a beszerzések értékénél a konzorciumi tagok számával egyenlő arányban elosztott érték szerepel. A konzorciumban elnyert összegek esetében emiatt tört értékek is előfordulnak.</li>
                <li>A cégek alvállalkozóként is részt vehettek a közbeszerzésben, azonban az alvállalkozókról nem áll rendelkezésre teljes körű információ, ezért ezeket a bevételeket nem vettük figyelembe.</li>
                <li>A közbeszerzési adatok elemzése során számos hibát és hiányosságot azonosítottunk az eredménytájékoztató hirdetményekben közzétett információkban, például hibásan közölt értékek jelzése kapcsán, mely a felületen megjelenített cégek kiválasztását is befolyásolhatta. Ezért 23 céget az adatok pontatlansága miatt nem szerepeltetünk az adatokban, noha a kiválasztási feltételeinknek megfeleltek volna.</li>
                </ul>
                <h3>3. Közhatalmi kapcsolatok</h3>
                <ul>
                <li>A politikai befolyás csak ritkán mutatható ki adatokkal, számos esetben a közhatalmi tituluson vagy állami cégekben vállalt szerepen túlmutató informális kapcsolatról van szó. Adatszinten nem kimutathatóak a baráti, szomszédsági vagy családi kapcsolatok, jelentőségüket azonban mi sem vitatjuk, és sajtóinformációk nyomán ezekkel külön elemzésekben foglalkozunk.</li>
                </ul>
